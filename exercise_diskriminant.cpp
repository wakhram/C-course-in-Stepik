#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    int a, b, c;
    cin >> a >> b >> c;
    double x1, x2;
    double d = sqrt((b*b-4*a*c));
    
    if (d > 0) {
        x1 = (-b+d)/(2*a);
        x2 = (-b-d)/(2*a);
        if (x1 == (double) x1 && x2 == (double) x2)
            cout << x1 << " " << x2 << endl;
    }
    else if (d == 0) {
        cout << -b/(2*a) << " " << -b/(2*a) << endl;
    }
    else {
        cout << "No real roots" << endl;
    }
    
    
    return 0;
}